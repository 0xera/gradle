<div id="about">

<h2>Gradle is a build system</h2>
    <p>A very flexible general purpose build tool like Ant.
    >Switchable, build-by-convention frameworks a la Maven, for Java, Groovy and Scala projects. But we never lock you
        in!
    <i>Groovy</i> build scripts.</p>
<h2>Gradle does some stuff</h2>
    <p>Powerful support for multi-project builds.
    Powerful dependency management (based on Apache Ivy).</p>
<h2>Gradle does some other stuff</h2>
    <p>Full support for your existing Maven or Ivy repository infrastructure.
    Support for transitive dependency management without the need for remote repositories and <tt>pom.xml</tt>
        or <tt>ivy.xml</tt> files (optional).
    </p>
    <p>Ant tasks and builds as first class citizens.</p>
<h2>Gradle is free</h2>
