<h1>About Gradle</h1>
<p>
    Gradle is a build system which provides:
</p>
<ul>
    <li>A very flexible general purpose build tool like Ant.</li>
    <li>Switchable, build-by-convention frameworks a la Maven (for Java and Groovy projects). But we never lock you
        in!
    </li>
    <li>Powerful support for multi-project builds.</li>
    <li>Powerful dependency management (based on Apache Ivy).</li>
    <li>Full support for your existing Maven or Ivy repository infrastructure.</li>
    <li>Support for transitive dependency management without the need for remote repositories and <tt>pom.xml</tt>
        or <tt>ivy.xml</tt> files (optional).
    </li>
    <li>Ant tasks as first class citizens.</li>
    <li><i>Groovy</i> build scripts.</li>
</ul>

Our 150+ pages <a href="http://www.gradle.org/${currentRelease}/docs/userguide/userguide.html">user guide</a>
(<a href="http://www.gradle.org/${currentRelease}/docs/userguide/userguide.pdf">pdf</a>)
contains tutorials, a detailed overview and in-depth coverage.

<p>The Gradle project itself is highly automated by its Gradle build. <a href="http://gradle.org/ci-server.html">As
    documented</a>, it runs happily on Teamcity and Bamboo (where Gradle is not pre-installed).
</p>
